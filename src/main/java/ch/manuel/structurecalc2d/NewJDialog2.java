    //Button pressed: Hinzufügen
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String str1, str2;
        boolean cond = true;
        double mod = 0;
        double surf = 0;
        
        //E-Modul auslesen
        str1 = jTextField1.getText();
        if (MyUtilities.isNumeric(str1)) {
            surf = Double.parseDouble(str1);
        } else {
            cond = false;
            MyUtilities.getErrorMsg("Eingabefehler", "Eingabe überprüfen: " + str1);
        }
        //Fläche auslesen
        str2 = jTextField2.getText();
        if (MyUtilities.isNumeric(str2)) {
            mod = Double.parseDouble(str2);
        } else {
            cond = false;
            MyUtilities.getErrorMsg("Eingabefehler", "Eingabe überprüfen: " + str2);
        }
        
        //Falls kein Fehler beim Einlesen: Eintragen Liste "listSections"
        if (cond) {
            addValue(surf, mod);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

