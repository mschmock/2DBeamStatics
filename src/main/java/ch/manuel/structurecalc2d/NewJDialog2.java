    // Beschriftung Titel Tabelle
    final private String titel_11 = "Nr.";
    final private String titel_12 = "Fläche A [mm2]";
    final private String titel_13 = "E-Modul [MPa]";
    final private int defaultNumbersOfRows = 0;
    
    //Wertepaar in erstellen und in Tabelle einfügen
    public static void addValue(double surf, double mod) {
        Structure.getSections().add(new Double[] {surf, mod});
        setContentTable();
    }
    
    // Methode zum Einfügen der Tabelle in das entsprechende ScrollPanes
    private void addTable() {
        // Tabelle 1
        tableModel1 = new javax.swing.table.DefaultTableModel(new String[]{titel_11, titel_12, titel_13}, defaultNumbersOfRows);
        jTable1 = new javax.swing.JTable();
        jTable1.setModel(tableModel1);
        
        //jTable1.setColumnSelectionAllowed(false);     //Nicht notwendig, da "setEabled = false"
        //jTable1.setRowSelectionAllowed(true);         //Nicht notwendig, da "setEabled = false
        jTable1.setEnabled(false);
        
        jTable1.setPreferredSize(jScrollPane1.getSize());
        jTable1.getTableHeader().setReorderingAllowed(false);
        jTable1.getColumnModel().getSelectionModel().setSelectionMode(javax.swing.ListSelectionModel.SINGLE_INTERVAL_SELECTION);
        jTable1.getColumnModel().getColumn(0).setResizable(false);
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(40);
        jTable1.getColumnModel().getColumn(1).setResizable(false);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(140);
        jTable1.getColumnModel().getColumn(2).setResizable(false);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(140);
        
        jScrollPane1.setViewportView(jTable1);
    }

    //Methode zum Füllen der Tabelle mit E und A
    public static void setContentTable() {
        //Tabelle leeren
        for(int i = (tableModel1.getRowCount() - 1); i >= 0; i--){
            tableModel1.removeRow(i);
        }
        
        //Tabelle mit Werten füllen
        for (int i = 0; i < Structure.getSections().size(); i++) {
            tableModel1.addRow(new Object[]{""});   //Neue Reihe
            jTable1.setValueAt(i+1, i, 0);                                      //1. Spalte: Nr
            jTable1.setValueAt(Structure.getSections().get(i)[0], i, 1);        //2. Spalte: Fläche
            jTable1.setValueAt(Structure.getSections().get(i)[1], i, 2);        //3. Spalte: E-Modul
        }
        jTable1.setPreferredSize(new java.awt.Dimension(TABLE_WIDTH, tableModel1.getRowCount()*jTable1.getRowHeight()));
    }
    
    //Eingabefenster zum Löschen Querschnitt (A und E) "Section"
    public static void getSectionDelDialog(){
        String txt;                     //Rückgabe Dialog
        int nb = Structure.getSections().size();    //Anzahl Objekt
        String obj[] = new String[nb];  //Liste für Auswahl erstellen "section"
        
        //Wenn kein Eintrag in der Tabelle -> Keine Aktion durchführen
        if (nb != 0) {
            //Auswahl "selection" erstellen
            for (int i = 0; i < nb; i++){
                obj[i] = Integer.toString(i+1);
            }

            txt = (String) JOptionPane.showInputDialog(
                    null,                           //parentComponent
                    "Querschnitt Wählen:",          //message
                    "Querschnitt löschen",          //title
                    JOptionPane.PLAIN_MESSAGE,      //message type
                    null,                           //icon
                    obj,                            //selection Values
                    obj[0]);                         //initial Value

            if ( txt == null ){     //Dialog Schliessen bei Abbruch: True
                //nichts...
            } else {
                 //Reihe in Tabelle löschen
                int pos = Integer.parseInt(txt) - 1;
                Structure.getSections().remove(pos);
                setContentTable();
            }
        }
    }
    
    
